<h2>Niveau rémunération ?</h2>

<p>Environ 1 850 € brut par mois pour un chef de projet numérique débutant, avec une progression qui peut être rapide selon la réussite des projets.
<br>
Les salaires des chefs de projet numérique varient selon leur expérience et la taille de l’entreprise.</p>

<h2>Ai-je les qualités et compétences nécessaires ?</h2>

Il doit prendre en compte les compétences de chacun et leur donner des missions cohérentes avec cela. À l’écoute, il possède de grandes capacités d’organisation. Il doit également être rigoureux. C’est même le garant de la rigueur en termes de temps, de budget, de ressources… du projet.

Très intéressé par le domaine des technologies, il a des compétences techniques en informatique et il sait de quoi il parle avec ses collaborateurs. Ce sont les formations informatiques, ingénieurs, ou encore jeux vidéos qui donnent ces compétences au chef de projet numérique.

<h2>Où puis-je exercer ? Paris, Lyon ou à la campagne ?</h2>


Il peut travailler dans une entreprise web et être chargé de différents projets à la suite. Il peut-être également  prestataire et travailler sur les projets d’entreprises clientes. Mais il est également possible de travailler en tant que free-lance et de mener ainsi plusieurs projets de front.

Si tu a toute ces qualités lance toi, les métiers du numérique évoluent tout les jours.
