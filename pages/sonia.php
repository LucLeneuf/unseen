        <div>
            <h2> Ces outils de créations </h2>

            <p>
                <span class="line">
                Un moniteur. <br>
                Un nuancier pantone ( <span class="lightgrey">palette de couleurs</span> ). <br>
                Une tablette graphique. <br>
                Une imprimante ( <span class="lightgrey"> multifonction, scanner, 300 dpi pour les photos / 600 dpi pour les illustrations </span> ). <br>
                Un ou plusieurs stockages externes ( <span class="lightgrey" > Clé usb, Disque dur externe, Cloud, Réseaux internet </span> ). <br>
                Des logiciels comme la suite Adobe ( <span class="lightgrey"> Photoshop, Illustrator, Dreamweaver </span> ) pour la création et retouche d’images, vidéos, photos. 
                </span> <br><br>

                Le crayon et le papier jouent toujours un rôle très important, surtout en brainstorming. Ils nous permettent de pouvoir laisser notre créativité s’exprimer, de travailler dans n’importe quelle condition comme la réactivité lorsque l’inspiration survient, ou tout ce qui nous entoure devient source d’inspiration, et pas besoin de craindre la panne de batterie ou tout autre problème technique.
            </p>
        
                <img src="../assets/images/pictures/photo_7.jpg" alt="Artiste qui réalise des esquisses"> 

            <p>
                Le fait de ne pas être distrait par les réseaux sociaux est un plus avec le papier, car il ne voue enverra jamais une notification pour vous prévenir. Et enfin, pour trouver votre propre source d’inspiration, vous devez vous documenter. Vous suivez forcément les dernières tendances, les avancées dans votre secteur, les types de design qui ont du succès, les inventions des uns et des autres. <br> <br>
                
                Pourtant, au moment de vous pencher sur un nouveau projet, vous devez réussir à garder tout cela dans l’arrière-plan de votre cerveau. Le danger encouru, à trop vous renseigner sur les travaux des autres, c’est de vous laisser phagocyter par eux. Inconsciemment, vous les laissez vous influencer, et cela d’autant plus qu’il vous suffit d’un clic de souris pour les visionner au beau milieu de votre propre création. Un cahier de dessin vous affranchit de cette tentation et vous aide à rester original. 
            </p>
