                <h1>Le Pentesteur, Le Corsaire des Hackers</h1>
                <p> Article réalisé et rédigé par <a href="https://github.com/WilliamsChazal" target="_blank"><span class="lightgrey">Williams C</span>.</a></p>
            </div>

            <div>
                <p>Le métier de « <span class="bold">Pentesteur</span> » peut aussi être assimilé à du « <span class="bold">Ethical hacking</span> » ou Piratage éthique en français car il utilise les mêmes méthodes que les autres hackers mais sans arrières pensées malveillante et au profit d’entreprise ou même d’Etats.</p>

                <img src="../assets/images/pictures/williams-2.png" alt="couv">

                <p>Le pentesteur est un professionnel de la sécurité informatique. Il évalue les système de prévention d’intrusion et de sécurité d’une entreprise avec pour objectif de limiter les pertes, le vol et la corruption des données d’un produit ou d’un système.</p>
            </div>
        </div>

        <div>
            <h2 class="red">Son rôle et ses missions</h2>

            <p>Son rôle est de contrôler la sécurité des applications tant bien mobiles que le backend des sites qui enregistrent des données confidentielles ( <span class="lightgrey">numéro de cartes bancaires par exemple</span> ). <br><br>

            Il analyse et contrôle la sécurité en opérant des tests d’intrusions ( <span class="lightgrey">attaques contrôlées</span> ), d’où son nom « Pentester » qui est la contraction de « penetration test ». Il analyse les failles de sécurité d’un système pour par la suite procéder à des testes d’intrusion, autrement dit des attaques contrôlées grandeur nature. <br><br>

            En amont de l’intrusion contrôlée, le Pentesteur effectue une multitudes de recherche sur les potentielles vulnérabilités techniques et humaines exploitables. Il collecte et répertorie les données sensibles pouvant ou ayant déjà amenées des incidents de cybersécurité. <br><br>

            A la suite de sa collecte de données, il met en place et organise des tests d’intrusions, tels qu’ils pourraient avoir lieu par des tiers. Dans un deuxième temps, il va évaluer le niveau de compromission et de perte des données, puis il analyse les risques de corruption liés aux différents tests d’intrusions réussis. Après avoir effectuer ses missions d’Hacking éthique, il va apporter des préconisations pour faire évoluer et améliorer le système précédemment testé. De plus il peut conseiller ses clients à propos de systèmes d’alertes qu’il est possible de mettre en place en cas d’intrusion dans le but de sécuriser les données accessibles.</p>
        </div>

        <div>
            <h2 class="red">Compétences et connaissances</h2>

            <p>Le pentester exerce directement au sein des entreprises ( <span class="lightgrey">les startups comme les grands groupes</span> ), de l’administration ou des cabinets de conseils spécialisés en Sécurité des Systèmes d’informations.</p>

            <img src="../assets/images/pictures/williams-couv.jpg" alt="couv">

            <p>
                Le métier de pentester nécessite des connaissances solides en réseau, sécurité informatique ( <span class="lightgrey">cryptographie, système de codage, audit de sécurité réseau et web</span> ), développement logiciel et systèmes informatiques ( <span class="lightgrey">systèmes, système industriels</span> ). En bref, il faut être en mesure de bien connaître les systèmes auxquels on va s’attaquer ! <br><br><br>


                Par ailleurs, les tests d’intrusion étant le plus souvent réalisés de manière automatisée avec des applications logicielles, il faut savoir programmer, par exemple en Python. De bonnes connaissances en Linux et ses distributions axées sécurité sont également très utiles. <br><br>

                De plus le Pentesteur doit communiquer à ses clients des préconisations et des conseils, il se doit d’avoir du savoir-être et de la psychologie pour présenter les failles d’un système à l’équipe l’ayant conçu et les amener à le modifier, sans provoquer d’animosité et ni de conflits.
            </p>
        </div> 
        
        <div>
            <h2 class="red">Formation et évolution</h2>

            <p>
                Le métier de pentester nécessite un bon niveau en informatique. Il peut faire divers formations <br><br><br>


                <span class="bold"> niveau bac + 3 </span> <br><br>
            
                Licence professionnelle métiers de l’informatique : administration et sécurité des systèmes et des réseaux <br>
                Différents parcours : cybersécurité ( <span class="lightgrey">Université de Bourgogne</span> ) BUT Informatique <br><br><br>

          
                <span class="bold">niveau bac + 5</span> <br><br>
           
                Diplôme d’ingénieur ( <span class="lightgrey">Isep</span> ) <br>
                Master en informatique avec spécialisation en cybersécurité <br><br><br>
          

                Des certifications en sécurité/produits peuvent être parfois exigées. Parmi les plus courantes : OSCP ( <span class="lightgrey">Offensive security certified professional</span> ). <br>
                De plus Comme pour beaucoup de nouveaux métiers, il y a aussi nombreux autodidactes passionnés par l’informatique qui exercent ce métier. <br>
                Pour exercer de métier de Penteste, il faut posséder une véritable éthique personnelle, indispensable pour évoluer dans le domaine de la sécurité informatique.
            </p>
