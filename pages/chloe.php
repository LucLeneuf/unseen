<h1>Le métier de développeur web</h1>
<p>par <a href="https://github.com/chloevth" target="_blank">Chloé V.</a></p>
<img src="../assets/images/pictures/chloe-2.png" alt="couv">
<h2>Qu’est-ce qu’un développeur web ?</h2>
<p>Le développeur rédige le code informatique d’un produit / site / programme répondant aux exigences du cahier des charges rédigé par le chef de projet en fonction des exigences du client et des besoins fonctionnels.
Il analyse les besoins, choisit la solution technique la mieux adaptée et développe les fonctionnalités du site web ou de l’application web de sa phase d’étude à son intégration.</p>


<h2>Autres missions</h2>
<ul>
<li>corriger les problèmes des projets développés sans interrompre le fonctionnement / assurer le support technique</li>
<li>former le client à la réception du site</li>
<li>rédiger notices et guides pour les utilisateurs</li>
</ul>
</div>
<div>
<h2>Les catégories de développeur web</h2>

<h3>Développeur back-end</h3>
<p>Il s’occupe de la programmation du côté du serveur : il crée le code et les programmes qui alimentent le serveur du site web, les bases de données et toutes les applications que contient le site internet.</p>

<h4>Il utilise les différents langages côté serveur</h4>
<ul>
<li>PHP</li>
<li>Python</li>
<li>Java</li>
<li>Ruby</li>
<li>SQL (pour gérer les données dans les bases de données des sites)</li>
</ul>

<h3>Développeur front-end</h3>
<p>Il se charge de la conception du site web côté client et écrit le code nécessaire pour l’implémenter sur le web. </p>

<span>Il utilise les différents langages côté client</span>
<ul>
<li>HTML</li>
<li>CSS</li>
<li>JavaScript</li>
</ul>
<span>Il peut également réaliser</span>
<ul>
<li>le référencement naturel (SEO)</li>
<li>les tests d’utilisabilité, d’accessibilité et de performances</li>
</ul>

<h3>Développeur full stack</h3>
<p>Il réunit les 2 facettes front-end et back-end.</p>

<h3>Développeur web mobile</h3>
<p>Il effectue la réalisation technique et le développement informatique d’applications pour mobiles, smartphones et autres tablettes.
<br>
Ses responsabilités sont de tenir compte des spécificités de l’affichage des applications sur les différents outils.<p>
</div>
<div>
<img src="../assets/images/pictures/chloe-couv-1.png" alt="couv">
<h3>Pour cela, il utilise différents langages selon les systèmes d’exploitation</h3>
<ul>
<li>C / Objective C / HTML5 (iOS)</li>
<li>C / C# / PHP / Java (Android)</li>
</ul>
<span>Deux possibilités s’offrent à lui</span>
<ul>
<li>Adapter un site internet classique pour une application mobile</li>
<li>Créer une application mobile qui n’a pas de site internet existant</li>
</ul>

<h3>Qualités recherchées</h3>
<ul>
<li>polyvalence</li>
<li>autonomie</li>
<li>respect des délais</li>
<li>veille technologique</li>
</ul>

<h3>Où exerce t-on ces métiers ?</h3>
<p>Cette activité s’exerce au sein de sociétés de services, d’entreprises, de collectivités territoriales, d’entreprises publiques, en indépendant etc.
<br>
Elle varie selon la taille de la structure (polyvalence, spécialisation en gestion du projet, programmation, recettes, etc.) et la responsabilité (développement, projet, etc).
<br>
Le salaire est également fonction de ces différents paramètres. En moyenne, un développeur web junior peut percevoir une rémunération brute mensuelle de 2100 €. Mais certains atteignent les étoiles tels les développeurs de la Silicon Valley qui peuvent recevoir un salaire de 500 000 $ … cela laisse rêveur !</p>

<h3>Pour en savoir plus</h3>
