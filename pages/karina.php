                <h1>Le métier de rédacteur web</h1>
                <p> Article réalisé et rédigé par <a href="https://github.com/KarinaEly" target="_blank"><span class="lightgrey">Karina E</span>.</a></p>
            </div>

            <p>Le Rédacteur Web est en charge de la rédaction de contenus pour un ou plusieurs sites web. Ce métier relève tout d’abord de compétences linguistiques et d’une aisance d’écriture.</p>

            <img src="../assets/images/pictures/karina-couv.jpg" alt="couv">

            <p> Celui-ci peut être spécialisé sur un sujet d’expertise, ce qui lui donne une légitimité pour écrire des contenus parfois très spécifiques : cuisine, juridiction, RH, actualités. Le rédacteur web travaille généralement sous la direction d’un responsable éditorial en charge d’élaborer une stratégie de contenu spécifique à chaque plateforme web.</p>
        </div>

        <div>
            <h2>Les missions</h2>

            <p>
            <span class="bold"> Le rédacteur web rédige en respectant plusieurs étapes, il doit :</span> <br><br>

                <span class="line">
                Etudier le sujet transmis par le client <br>
                Effectuer plusieurs recherches pour en extraire les informations importantes provenant de sources fiables <br>
                Veiller à la fiabilité et l’exactitude des informations <br>
                Choisir les bons mots clés, les requêtes principales et secondaires <br>
                Rédiger le contenu et reformule certaines informations en citant ses sources <br> <br>
                Veiller à rédiger un contenu texte adapté à la demande et aux besoins de son client <br>
                Chercher des visuels attractifs pour illustrer le contenu texte <br>
                Intégrer le contenu texte et les visuels dans la console d’administration <br>
                Optimiser le contenu texte et les visuels pour répondre aux critères du référencement et aux exigences de Google <br>
                Procéder à la publication après un dernier contrôle
                </span>
            </p>
        </div>

        <div>
            <h2> Compétences d’un rédacteur web </h2>

            <p>
                <span class="line">
                Aisance rédactionnelle, <br>
                Maîtrise parfaite de la langue dans laquelle il travaille et des règles de syntaxe / d’orthographe <br>
                Bonne connaissance de l’environnement web et des tendances / pratiques <br>
                Maîtrise des outils de bureautique ( <span class="lightgrey">tel que le pack office</span> ) <br>
                Expertise en « content management », spécifique au web <br>
                Connaissances dans le référencement naturel ( <span class="lightgrey">SEO</span> ) <br>
                Compétences en structuration et en intégration web ( <span class="lightgrey">HTML et CSS</span> ) <br>
                Aisance sur les réseaux sociaux et dans le partage de contenu
                </span>
            </p>
        </div>

        <div>   
            <p>
                <span class="bold">Les qualités d’un rédacteur web</span> <br><br>

                <span class="line">
                Culture générale ainsi que goût pour la littérature et l’actualité <br>
                Créativité <br>
                Autonomie et efficacité <br>
                Curiosité et ouverture d’esprit <br>
                Force de proposition
                </span>
            </p>
