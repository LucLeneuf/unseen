        <div>  
            <h2>Evolution du métier de rédacteur web</h2>
            <p>
                Les compétences d’un Rédacteur Web peuvent se rapprocher de celles d’un Community Manager, qui lui aussi pourra contribuer à la rédaction de contenu. Cependant, certains contenus très spécifiques demanderont l’expertise d’un professionnel du secteur pour rédiger des articles pertinents.<br>
                Ses années d’expériences lui permettront de passer le cap de rédacteur confirmé et ses réalisations lui donneront une reconnaissance dans la profession.
            </p>

            <img src="../assets/images/pictures/karina-2.jpg" alt="">

            <p>
                <span class="bold"> Quels sont les inconvénients de ce métier ?</span> <br><br>
                Il faut passer beaucoup de temps à lire et se documenter pour bien comprendre un sujet ou un domaine et pouvoir le maîtriser avant de rédiger quoi que ce soit. C’est un métier qui nécessite du temps d’analyse, de compréhension et de réflexion avant de pouvoir écrire ou livrer un article. Cela peut parfois être stressant, les rédacteurs web étant parfois considérés comme des machines à écrire. Or, la rédaction en général demande du temps.
            </p>

            <p>
                <span class="bold">Où exerce un rédacteur web ?</span> <br><br>

                En freelance, c’est-à-dire à son propre compte, généralement sous le statut d’auto-entrepreneur ( <span class="lightgrey">Externalisation de l’activité</span> ) <br>
                En agence web ou dans une entreprise spécialisée dans la communication digitale ( <span class="lightgrey">Externalisation de l’activité</span> ) <br>
                Directement chez l’annonceur, au sein d’une entreprise ou d’une association
            </p>
       
            <p>
            <span class="bold">Sa semaine type</span> <br><br>
                <span class="line">
                50% de création de contenus <br>
                20% de publication multiplateformes <br>
                15% d’arrimage équipe Marketing <br>
                15% de promotion des médias sociaux <br>
                Directement chez l’annonceur, au sein d’une entreprise ou d’une association
                </span> 
            </p>
        </div>

        <div>
            <h2>Devenir rédacteur web</h2>
            <p>
                Aucune formation spécifique n’est exigée pour devenir Rédacteur Web. Seule la justification d’une maîtrise linguistique et rédactionnelle sera demandée. Des tests de rédaction de contenu pourront être demandé afin d’évaluer la capacité à délivrer un contenu adapté aux exigences demandées. Néanmoins, les employeurs regarderont avec intérêt les diplômes, qui pourront justifier des connaissances spécifiques et une bonne culture générale. <br><br><br>
            
                <span class="bold">Son salaire</span> <br><br>

                Rémunération correspondant à un poste en CDI : Junior : 20K €/ an, Senior : 35K €/ an, Expert : 50K €/ an.</p>
